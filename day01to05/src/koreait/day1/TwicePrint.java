package koreait.day1;

public class TwicePrint {

	public static void main(String[] args) {
		System.out.println("트와이스");
		System.out.println("-----------------");
		System.out.printf("%-10s %s\n","사나","1996.12.29");
		System.out.printf("%-10s %s\n","지효","1997.02.01");
		System.out.printf("%-10s %s\n","미나","1997.03.24");
		System.out.printf("%-10s %s\n","다현","1998.05.28");
		System.out.printf("%-10s %s\n","채영","1999.04.23");
		System.out.printf("%-10s %s\n","쯔위","1996.06.14");
	}
	// \n, \t : 특정기능문자 (이스케이프 문자)
}
