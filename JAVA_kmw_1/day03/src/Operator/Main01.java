package Operator;

public class Main01 {

	public static void main(String[] args) {

		int num1 = 12;
		int num2 = 8;
		
		int result1 = num1 + num2;  // 20
		int result2 = num1 - num2;  // 4
		int result3 = num1 * num2;  // 96
		int result4 = num1 / num2;  // 1
		int result5 = num1 % num2;  // 4
		
		System.out.println("12+8 = " + result1);
		System.out.println("12-8 = " + result2);
		System.out.println("12*9 = " + result3);
		System.out.println("12/8 = " + result4);
		System.out.println("12%8 = " + result5);
		
		
	}

}
